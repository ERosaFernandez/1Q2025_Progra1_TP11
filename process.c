void process (void (*pS)(char*, char*), uint8_t (*pR)(char*), int8_t select, char* pines [], uint8_t max, char* on, char* off){
	uint8_t i;

	switch (select){
	case CLEAR:
		for (i = 0; i < max; i++) (*pS) (pines [i], off);
		break;
	case SET:
		for (i = 0; i < max; i++) (*pS) (pines [i], on);
		break;
	case TOGGLE:
		for (i = 0; i < max; i++) {
			if ((*pR) (pines [i])) (*pS) (pines [i], off);
			else (*pS) (pines [i], on);
		}
		break;
	default:
		if ((*pR) (pines [select])) (*pS) (pines [select], off);
		else (*pS) (pines [select], on);
	}
}


